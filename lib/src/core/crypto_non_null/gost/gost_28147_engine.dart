class GOST28147Engine {
  static const int defaultBlockSize = 8;
  List<int> _workingKey = [];
  bool _forEncryption = false;

  List<int> _state = sboxDefault;

  //
  // pre-defined sbox table
  //
  static Map<String, List<int>> sBoxes = {
    "Default": sboxDefault,
    "E_TEST": eSboxTest,
    "E_A": eSboxA,
    "E_B": eSboxB,
    "E_C": eSboxC,
    "E_D": eSboxD,
    "D_TEST": dSboxTest,
    "D_A": dSboxA,
  };

  static void addSBox(String sBoxName, List<int> sBox) {
    sBoxes[sBoxName.toUpperCase()] = sBox;
  }

  static _arraycopy(
      List<int> inp, int inOff, List<int> out, int outOff, int length) {
    for (int i = 0; i < length; i++) {
      if (i + inOff >= inp.length) break;
      out[i + outOff] = inp[i + inOff];
    }
  }

  void init(bool forEncryption, CipherParameters params) {
    if (params is ParametersWithSBox) {
      final ParametersWithSBox param = params;
      //
      // Set the S-Box
      //
      final sBox = param.sBox;
      if (sBox.length != sboxDefault.length) {
        throw Exception("invalid S-box passed to GOST28147 init");
      }

      _state = List.filled(sBox.length, 0);
      GOST28147Engine._arraycopy(sBox, 0, _state, 0, sBox.length);

      //
      // set key if there is one
      //
      final parameters = param.parameters;
      if (parameters is KeyParameter) {
        _workingKey = _generateWorkingKey(forEncryption, parameters.key);
      } else if (parameters != null) {
        throw Exception("invalid parameter passed to GOST28147 init");
      }
    } else if (params is KeyParameter) {
      _workingKey = _generateWorkingKey(forEncryption, params.key);
    } else {
      throw Exception("invalid parameter passed to GOST28147 init");
    }
  }

  List<int> _generateWorkingKey(bool forEncryption, List<int> userKey) {
    _forEncryption = forEncryption;

    if (userKey.length != 32) {
      throw Exception(
          "Key length invalid. Key needs to be 32 byte - 256 bit!!!");
    }

    List<int> key = List.empty(growable: true);
    for (var i = 0; i != 8; i++) {
      key.add(_bytesToint(userKey, i * 4));
    }

    return key;
  }

  //array of bytes to type int
  static int _bytesToint(List<int> inp, int inOff) {
    return ((inp[inOff + 3] << 24) & 0xff000000) +
        ((inp[inOff + 2] << 16) & 0xff0000) +
        ((inp[inOff + 1] << 8) & 0xff00) +
        (inp[inOff] & 0xff);
  }

  String get algorithmName => "GOST28147";

  int get blockSize => GOST28147Engine.defaultBlockSize;

  int processBlock(List<int> inp, int inOff, List<int> out, int outOff) {
    if (_workingKey.isEmpty) {
      throw Exception("GOST28147 engine not initialised");
    }

    if ((inOff + GOST28147Engine.defaultBlockSize) > inp.length) {
      throw Exception("input buffer too short");
    }

    if ((outOff + GOST28147Engine.defaultBlockSize) > out.length) {
      throw Exception("output buffer too short");
    }

    _gostFunc(_workingKey, inp, inOff, out, outOff);

    return GOST28147Engine.defaultBlockSize;
  }

  void reset() {}

  int _mainStep(int n1, int key) {
    int cm = (key + n1); // CM1
    // S-box replacing
    int om;
    om = _state[0 + ((cm >> (0 * 4)) & 0xF)] << (0 * 4);
    om += _state[16 + ((cm >> (1 * 4)) & 0xF)] << (1 * 4);
    om += _state[32 + ((cm >> (2 * 4)) & 0xF)] << (2 * 4);
    om += _state[48 + ((cm >> (3 * 4)) & 0xF)] << (3 * 4);
    om += _state[64 + ((cm >> (4 * 4)) & 0xF)] << (4 * 4);
    om += _state[80 + ((cm >> (5 * 4)) & 0xF)] << (5 * 4);
    om += _state[96 + ((cm >> (6 * 4)) & 0xF)] << (6 * 4);
    om += _state[112 + ((cm >> (7 * 4)) & 0xF)] << (7 * 4);

    return (om << 11) | ((om & 0xFFFFFFFF) >> (32 - 11)); // 11-leftshift
  }

  void _gostFunc(List<int> workingKey, List<int> inp, int inOff, List<int> out,
      int outOff) {
    int n1;
    int n2;
    int tmp; //tmp -> for saving n1
    n1 = _bytesToint(inp, inOff);
    n2 = _bytesToint(inp, inOff + 4);

    if (_forEncryption) {
      for (int k = 0; k < 3; k++) {
        // 1-24 steps
        for (int j = 0; j < 8; j++) {
          tmp = n1;
          n1 = n2 ^ _mainStep(n1, workingKey[j]); // CM2
          n2 = tmp;
        }
      }
      for (int j = 7; j > 0; j--) {
        // 25-31 steps
        tmp = n1;
        n1 = n2 ^ _mainStep(n1, workingKey[j]); // CM2
        n2 = tmp;
      }
    } else {
      //decrypt
      for (int j = 0; j < 8; j++) {
        // 1-8 steps
        tmp = n1;
        n1 = n2 ^ _mainStep(n1, workingKey[j]); // CM2
        n2 = tmp;
      }
      for (int k = 0; k < 3; k++) {
        //9-31 steps
        for (int j = 7; j >= 0; j--) {
          if ((k == 2) && (j == 0)) {
            break; // break 32 step
          }
          tmp = n1;
          n1 = n2 ^ _mainStep(n1, workingKey[j]); // CM2
          n2 = tmp;
        }
      }
    }

    n2 = n2 ^ _mainStep(n1, workingKey[0]); // 32 step (n1=n1)

    _intTobytes(n1, out, outOff);
    _intTobytes(n2, out, outOff + 4);
  }

  //int to array of bytes
  void _intTobytes(int num, List<int> out, int outOff) {
    out[outOff + 3] = ((num & 0xFFFFFFFF) >> 24) & 0xFF;
    out[outOff + 2] = ((num & 0xFFFFFFFF) >> 16) & 0xFF;
    out[outOff + 1] = ((num & 0xFFFFFFFF) >> 8) & 0xFF;
    out[outOff] = num & 0xFF;
  }

  static List<int> getSBox(String sBoxName) {
    var sBox = sBoxes[sBoxName.toUpperCase()];
    if (sBox == null) {
      throw Exception(
          "Unknown S-Box - possible types: \"Default\", \"E_Test\", \"E_A\", \"E_B\", \"E_C\", \"E_D\", \"D_Test\", \"D_A\".");
    }
    var copy = List.filled(sBox.length, 0);
    _arraycopy(sBox, 0, copy, 0, copy.length);
    return copy;
  }
